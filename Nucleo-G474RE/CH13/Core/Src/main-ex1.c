int main(void) {
  HAL_Init();
  Nucleo_BSP_Init();

  /* Initialize all configured peripherals */
  MX_DAC1_Init();

  HAL_DAC_Init(&hdac1);
  HAL_DAC_Start(&hdac1, DAC_CHANNEL_2);

  while(1) {
    int i = 0;
    while(i < 2000) {
      HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, i);
      HAL_Delay(1);
      i+=4;
    }

    while(i > 0) {
      HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, i);
      HAL_Delay(1);
      i-=4;
    }
  }
}

